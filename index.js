//Utilizando a variável descontinuada chamada VAR

var nome = 'Sora';

console.log(nome);

nome = 'Ogamy';

console.log(nome);

//Utilizando a variável LET

let nomeCompleto = 'Ogamy Sora';
let idade = 31;

console.log(nomeCompleto, idade);

//Variável Let e Var podem ser renomeadas

nomeCompleto = 'Sora Ogamy';
idade = 30;

console.log(nomeCompleto, idade);

//Utilizando a variável CONST

const email = 'sora.natsuki999@gmail.com';

console.log(email);

